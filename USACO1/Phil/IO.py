'''
Problem 3
Write a function that reads in multiple integers, and then print the average of them.

The first integer n represents the number of integers in the list, which followed by n number of integers in the second line.

Sample Input 1:

text
4
9 8 5 17

'''
repeat = int(input())
a = input().split()
avg = 0
for i in a:
    avg += int(a)
print(avg / repeat)